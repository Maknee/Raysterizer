        Error CubemapPrefiler::Setup(std::shared_ptr<CommonResources> common_resources_, CMShared<Texture> cubemap)
        {
            common_resources = common_resources_;

            vk::Device device = c.GetDevice();
            RenderFrame& render_frame = c.GetRenderFrame();

            auto current_frame = c.GetFrame();
            auto current_frame_index = c.GetFrameIndex();
            auto num_frames = c.GetNumFrames();

            size = cubemap->image->image_create_info.image_create_info.extent.width;

            cubemap_image_view = CommonResources::CreateImageView(cubemap->image, vk::ImageViewType::eCube, vk::ImageAspectFlagBits::eColor, 0, cubemap->image->image_create_info.image_create_info.mipLevels, 0, 6);
            c.SetName(cubemap_image_view, fmt::format("Prefiltered Src Image"));

            cubemap_texture = std::make_shared<Texture>(cubemap->image, cubemap_image_view, common_resources->nearest_sampler);

            image = CommonResources::CreateImage(vk::ImageType::e2D, PREFILTER_MAP_SIZE, PREFILTER_MAP_SIZE, 1, 5, 6,
                vk::Format::eR16G16B16A16Sfloat, VMA_MEMORY_USAGE_GPU_ONLY,
                vk::ImageUsageFlagBits::eSampled | vk::ImageUsageFlagBits::eStorage, vk::SampleCountFlagBits::e1,
                vk::ImageLayout::eUndefined, 0, nullptr, vk::ImageCreateFlagBits::eCubeCompatible);
            c.SetName(image, fmt::format("Prefiltered Dst Image"));

            image_view = CommonResources::CreateImageView(image, vk::ImageViewType::eCube, vk::ImageAspectFlagBits::eColor, 0, 5, 0, 6);
            c.SetName(image_view, fmt::format("Prefiltered Dst Image View"));

            texture = std::make_shared<Texture>(image, image_view, common_resources->trilinear_sampler);

            mip_image_views.resize(PREFILTER_MIP_LEVELS);
            mip_textures.resize(PREFILTER_MIP_LEVELS);
            sample_directions_ubos.resize(PREFILTER_MIP_LEVELS);

            for (int i = 0; i < PREFILTER_MIP_LEVELS; i++)
            {
                mip_image_views[i] = CommonResources::CreateImageView(image, vk::ImageViewType::eCube, vk::ImageAspectFlagBits::eColor, i, 1, 0, 6);
                c.SetName(mip_image_views[i], fmt::format("Prefiltered Image View Mip {}", i));
                
                mip_textures[i] = std::make_shared<Texture>(image, mip_image_views[i], common_resources->nearest_sampler);

                sample_directions_ubos[i] = AssignOrPanic(render_frame.CreateBuffer(MemoryUsage::CpuToGpu, vk::BufferUsageFlagBits::eUniformBuffer, sizeof(glm::vec4) * MAX_PREFILTER_SAMPLES, true));
                c.SetName(sample_directions_ubos[i], fmt::format("Sample Directions UBO {}", i));
            }

            PrecomputePrefilterConstants();

            {
                descriptor_sets.resize(PREFILTER_MIP_LEVELS);

                CMShared<DescriptorPool> dp = common_resources->global_descriptor_pools[current_frame_index];

                auto shader_module_create_infos = std::vector<ShaderModuleCreateInfo>
                {
                    ShaderModuleCreateInfo{Spirv{std::begin(kPREFILTER_SPIRV_data), std::end(kPREFILTER_SPIRV_data)}},
                };

                auto plci = PipelineLayoutCreateInfo
                {
                    shader_module_create_infos,
                };

                CMShared<PipelineLayoutInfo> pli = AssignOrPanic(c.Get(plci));
                for (int i = 0; i < PREFILTER_MIP_LEVELS; i++)
                {
                    CMShared<DescriptorSet> ds = AssignOrPanic(c.CreateDescriptorSet(DescriptorSetCreateInfo{ dp, pli }));
                    descriptor_sets[i] = ds;
                }
            }

            return NoError();
        }

        void CubemapPrefiler::Update(CMShared<CommandBuffer> command_buffer)
        {
            ScopedGPUProfileRaysterizer(command_buffer, "Cubemap prefilter SH");

            vk::Device device = c.GetDevice();
            RenderFrame& render_frame = c.GetRenderFrame();

            auto current_frame = c.GetFrame();
            auto current_frame_index = c.GetFrameIndex();
            auto num_frames = c.GetNumFrames();

            auto& cb = **command_buffer;

            CMShared<DescriptorPool> dp = common_resources->global_descriptor_pools[current_frame_index];

            {
                {
                    auto subresource_range = vk::ImageSubresourceRange{}
                        .setAspectMask(vk::ImageAspectFlagBits::eColor)
                        .setBaseMipLevel(0)
                        .setLevelCount(PREFILTER_MIP_LEVELS)
                        .setBaseArrayLayer(0)
                        .setLayerCount(6);

                    PanicIfError(command_buffer->SetImageLayout(image, vk::ImageLayout::eUndefined, vk::ImageLayout::eGeneral, subresource_range));
                }

                {
                    auto shader_module_create_infos = std::vector<ShaderModuleCreateInfo>
                    {
                        ShaderModuleCreateInfo{Spirv{std::begin(kPREFILTER_SPIRV_data), std::end(kPREFILTER_SPIRV_data)}},
                    };

                    auto plci = PipelineLayoutCreateInfo
                    {
                        shader_module_create_infos,
                    };

                    CMShared<PipelineLayoutInfo> pli = AssignOrPanic(c.Get(plci));
                    //CMShared<DescriptorSet> ds = AssignOrPanic(render_frame.Get(DescriptorSetCreateInfo{ dp, pli }));
                    CMShared<PipelineLayout> pl = AssignOrPanic(c.Get(pli));

                    ComputePipelineCreateInfo compute_pipeline_create_info{ pl };
                    CMShared<ComputePipeline> cp = AssignOrPanic(c.Get(compute_pipeline_create_info));

                    c.SetName(pl, "Cubemap sh projection projection pipeline layout");
                    c.SetName(cp, "Cubemap sh projection projection compute pipeline");

                    {
                        int32_t start_level = (size / PREFILTER_MAP_SIZE) - 1;

                        for (int mip = 0; mip < PREFILTER_MIP_LEVELS; mip++)
                        {
                            uint32_t mip_width = PREFILTER_MAP_SIZE * std::pow(0.5, mip);
                            uint32_t mip_height = PREFILTER_MAP_SIZE * std::pow(0.5, mip);

                            cb.bindPipeline(vk::PipelineBindPoint::eCompute, cp->pipeline);

                            CMShared<DescriptorSet> ds = descriptor_sets[mip];
                            PanicIfError(ds->Bind(0, 0, mip_textures[mip], vk::ImageLayout::eGeneral, vk::DescriptorType::eStorageImage));
                            PanicIfError(ds->Bind(0, 1, cubemap_texture, vk::ImageLayout::eReadOnlyOptimal, vk::DescriptorType::eCombinedImageSampler));
                            PanicIfError(ds->Bind(0, 2, sample_directions_ubos[mip]));

                            PanicIfError(render_frame.FlushPendingWrites(ds));

                            PushConstants push_constants;

                            push_constants.roughness = (float)mip / (float)(PREFILTER_MIP_LEVELS - 1);
                            push_constants.size = mip_height;
                            push_constants.start_mip_level = start_level;
                            push_constants.sample_count = sample_count;

                            PanicIfError(render_frame.BindPushConstant(command_buffer, pl, PointerView(push_constants)));
                            cb.bindDescriptorSets(vk::PipelineBindPoint::eCompute, pl->pipeline_layout, 0, ds->descriptor_sets, {});

                            cb.dispatch(mip_width / PREFILTER_WORK_GROUP_SIZE, mip_height / PREFILTER_WORK_GROUP_SIZE, 6);
                        }
                    }
                }

                {
                    auto subresource_range = vk::ImageSubresourceRange{}
                        .setAspectMask(vk::ImageAspectFlagBits::eColor)
                        .setBaseMipLevel(0)
                        .setLevelCount(PREFILTER_MIP_LEVELS)
                        .setBaseArrayLayer(0)
                        .setLayerCount(6);

                    PanicIfError(command_buffer->SetImageLayout(image, vk::ImageLayout::eGeneral, vk::ImageLayout::eShaderReadOnlyOptimal, subresource_range));
                }
            }
        }

        void CubemapPrefiler::SetSampleCount(uint32_t count)
        {
            if (sample_count != count)
            {
                sample_count = count;
                PrecomputePrefilterConstants();
            }
        }

        void CubemapPrefiler::PrecomputePrefilterConstants()
        {
            for (int mip = 0; mip < PREFILTER_MIP_LEVELS; mip++)
            {
                uint32_t mip_width = PREFILTER_MAP_SIZE * std::pow(0.5, mip);
                uint32_t mip_height = PREFILTER_MAP_SIZE * std::pow(0.5, mip);

                float roughness = (float)mip / (float)(PREFILTER_MIP_LEVELS - 1);

                std::vector<glm::vec4> samples;

                samples.resize(MAX_PREFILTER_SAMPLES);

                for (int i = 0; i < sample_count; i++)
                {
                    glm::vec2 Xi = hammersley(i, sample_count);
                    float     a = roughness * roughness;

                    float phi = 2.0f * glm::pi<float>() * Xi.x;
                    float cos_theta = sqrt((1.0f - Xi.y) / (1.0f + (a * a - 1.0f) * Xi.y));
                    float sin_theta = sqrt(1.0f - cos_theta * cos_theta);

                    // from spherical coordinates to cartesian coordinates - halfway vector
                    glm::vec3 H;
                    H.x = cos(phi) * sin_theta;
                    H.y = sin(phi) * sin_theta;
                    H.z = cos_theta;

                    samples[i] = glm::vec4(H, 0.0f);
                }

                void* mapped_ptr = sample_directions_ubos[mip]->GetPointerView().GetData();
                memcpy(mapped_ptr, samples.data(), sizeof(glm::vec4) * sample_count);
            }
        }
